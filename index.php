										<h3>Jae-Moon Jeong<h3>
										<h5>**Mechanical Design Lab. with Advanced Materials, KAIST**<h5>

									</header>
									<p>Jae-Moon Jeong is a Mechanical Engineering (ME) researcher of <a href="http://www.kaist.ac.kr">KAIST</a> (Korea Advanced Institute of Science and Technology). His detailed research topics can be divided into composite material manufacturing processes, and energy systems that use composite materials. He have published a total of 3 SCI papers, which were in JCR’s Top 10% journals on composite materials and related fields. He also registered 8 domestic patents and 2 international patents. His research has been presented in several conferences and organizations.</p>
									<p>	He has a academic background in composite materials, energy storage system. In the energy field, research has focused on designing a carbon electrode, which is one of the most important components in a redox flow battery. He developed a coating process that can prevent damage to the surface of the carbon felt electrode material and increase the activation site during the heat treatment process that dramatically improves the energy efficiency to increase the energy efficiency of the redox flow battery. Further, we suggested a “flow through” electrode model that maximizes the contact area between the electrolyte and electrode by designing a pattern on a carbon paper to solve the high defect rate of carbon felt; this is the conventional electrode material. To the best of our knowledge, we introduced the “pseudo-channel” effect related to the electrolyte flow in the electrode for the first time. This effect simultaneously improves energy and system efficiencies. This technology has pending patent applications abroad and gained considerable research interest in related industries and is in the process of transferring technology.</p>
									<p>	In his spare time, I love traveling and playing a pinao and do fun stuffs;</p>
